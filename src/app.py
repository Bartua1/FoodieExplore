# Helper functions for export
def convert_to_export_format(restaurants):
    """Converts SQLite integer booleans and other fields to final schema format."""
    exported = []
    for r in restaurants:
        r_dict = dict(r)
        # Convert SQLite is_promoted integer to boolean
        r_dict["is_promoted"] = True if r_dict.get("is_promoted") == 1 else False
        # Remove explorer specific fields
        r_dict.pop("validation_status", None)
        r_dict.pop("neighborhood", None)
        r_dict.pop("osm_id", None)
        exported.append(r_dict)
    return exported

def generate_sql_seed(restaurants):
    """Generates PostgreSQL INSERT statements matching the target global_restaurants schema."""
    if not restaurants:
        return ""
        
    sql_lines = [
        "-- SQL Seed Script for global_restaurants",
        "-- Generated by Neighborhood Explorer",
        "INSERT INTO global_restaurants (",
        "    id, name, description, cuisine_type, price_level, lat, lng, address, ",
        "    cover_image_url, is_promoted, promotion_end_at, created_at, updated_at, ",
        "    likes_count, views_count",
        ") VALUES"
    ]
    
    value_rows = []
    for r in restaurants:
        def escape(val):
            if val is None:
                return "NULL"
            escaped = str(val).replace("'", "''")
            return f"'{escaped}'"
            
        r_id = escape(r.get("id"))
        name = escape(r.get("name"))
        desc = escape(r.get("description"))
        cuisine = escape(r.get("cuisine_type"))
        price = str(r.get("price_level")) if r.get("price_level") is not None else "NULL"
        lat = str(r.get("lat"))
        lng = str(r.get("lng"))
        addr = escape(r.get("address"))
        cover = escape(r.get("cover_image_url"))
        is_promoted = "true" if r.get("is_promoted") == 1 else "false"
        prom_end = escape(r.get("promotion_end_at"))
        created = escape(r.get("created_at"))
        updated = escape(r.get("updated_at"))
        likes = str(r.get("likes_count") or 0)
        views = str(r.get("views_count") or 0)
        
        row_str = f"    ({r_id}, {name}, {desc}, {cuisine}, {price}, {lat}, {lng}, {addr}, {cover}, {is_promoted}, {prom_end}, {created}, {updated}, {likes}, {views})"
        value_rows.append(row_str)
        
    sql_lines.append(",\n".join(value_rows) + ";")
    return "\n".join(sql_lines)

